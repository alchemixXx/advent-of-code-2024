function buildMap(input) {
  const map = new Map();
  for (let i = 0; i < input.length; i++) {
    for (let j = 0; j < input[i].length; j++) {
      if (input[i][j] === '.') {
        continue;
      }

      if (!map.has(input[i][j])) {
        map.set(input[i][j], []);
      }

      map.get(input[i][j]).push([i, j]);
    }
  }

  return map;
}

function countAntinodesPart1(map, input) {
  let antinodes = new Set();
  for (const el of map.values()) {
    for (let i = 0; i < el.length; i++) {
      for (let j = i + 1; j < el.length; j++) {
        const [row1, col1] = el[i];
        const [row2, col2] = el[j];
        const rowDiff = row2 - row1;
        const colDiff = col2 - col1;
        const row3 = row2 + rowDiff;
        const col3 = col2 + colDiff;
        const row4 = row1 - rowDiff;
        const col4 = col1 - colDiff;
        if (!isOutOfBounds(row3, col3, input)) {
          antinodes.add(`${row3},${col3}`);
        }

        if (!isOutOfBounds(row4, col4, input)) {
          antinodes.add(`${row4},${col4}`);
        }
      }
    }
  }
  return antinodes.size;
}

function countAntinodesPart2(map, input) {
  let antinodes = new Set();
  for (const el of map.values()) {
    for (let i = 0; i < el.length; i++) {
      for (let j = i + 1; j < el.length; j++) {
        const [row1, col1] = el[i];
        const [row2, col2] = el[j];
        let rowDiff = row2 - row1;
        let colDiff = col2 - col1;

        antinodes.add(`${row1},${col1}`);
        antinodes.add(`${row2},${col2}`);

        if (rowDiff === 0) {
          colDiff = 1;
        }

        if (colDiff === 0) {
          rowDiff = 1;
        }

        let nextRow1 = row2 + rowDiff;
        let nextCol1 = col2 + colDiff;

        let prevRow1 = row2 - rowDiff;
        let prevCol1 = col2 - colDiff;

        while (!isOutOfBounds(nextRow1, nextCol1, input)) {
          antinodes.add(`${nextRow1},${nextCol1}`);
          nextRow1 += rowDiff;
          nextCol1 += colDiff;
        }

        while (!isOutOfBounds(prevRow1, prevCol1, input)) {
          antinodes.add(`${prevRow1},${prevCol1}`);
          prevRow1 -= rowDiff;
          prevCol1 -= colDiff;
        }
      }
    }
  }
  return antinodes.size;
}

function isOutOfBounds(row, col, input) {
  return row < 0 || row >= input.length || col < 0 || col >= input[0].length;
}